import { invoke } from "@tauri-apps/api/tauri";
import { open } from "@tauri-apps/api/dialog";
import Head from "next/head";
import styles from "../styles/Home.module.css";
import { useEffect, useState } from "react";
import * as fs from "@tauri-apps/api/fs";

const Home = () => {
  const [appDir, setAppDir] = useState<string>("");
  const openDialog = () => {
    open().then((files) => console.log(files));
  };

  const executeCommands = () => {
    invoke("simple_commnad");
  };

  useEffect(() => {
    (async () => {
      const { path } = await import("@tauri-apps/api");
      // path系のAPI
      const appDir = await path.appDir();
      setAppDir(appDir);
      const audioDir = await path.audioDir();
      const homeDir = await path.homeDir();
      const cacheDir = await path.cacheDir();
      const downloadDir = await path.downloadDir();
      const dataDir = await path.dataDir();
      const deskTopDir = await path.desktopDir();

      console.log(`appDir: ${appDir}`);
      console.log(`audioDir: ${audioDir}`);
      console.log(`homeDir: ${homeDir}`);
      console.log(`cacheDir: ${cacheDir}`);
      console.log(`downloadDir: ${downloadDir}`);
      console.log(`dataDir: ${dataDir}`);
      console.log(`deskTopDir: ${deskTopDir}`);

      await fs.createDir("images", {
        dir: fs.BaseDirectory.App,
        recursive: true,
      });
    })();
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <h1 className="text-3xl text-black-300 font-bold">tauri-sandbox</h1>
        <div className="pb-2">
          <h2 className="font-bold">Command</h2>
          <div>invoke rust command</div>
          <button
            className="rounded-md border border-transparent bg-indigo-600 px-1 py-1 text-base font-medium text-white hover:bg-indigo-700"
            onClick={executeCommands}
          >
            invoke
          </button>
        </div>

        <div className="pb-2">
          <h2 className="font-bold">Tauri API</h2>
          <div>open dialog</div>
          <button
            className="rounded-md border border-transparent bg-indigo-600 px-1 py-1 text-base font-medium text-white hover:bg-indigo-700"
            onClick={openDialog}
          >
            openDialog
          </button>
          <div>get local image</div>
          <div>file</div>
          <p>appDir:{appDir}</p>
        </div>
      </main>
    </div>
  );
};

export default Home;
